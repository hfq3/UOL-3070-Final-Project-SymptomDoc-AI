def get_kokoro_pipeline(lang_code: str = 'a') -> KPipeline:
    global _KOKORO_PIPELINE
    if _KOKORO_PIPELINE is None:
        if KPipeline is None:
            raise RuntimeError("kokoro package not installed")
        logger.info("Loading Kokoro TTS...")
        _KOKORO_PIPELINE = KPipeline(lang_code=lang_code)
        logger.info("Kokoro TTS loaded")
    return _KOKORO_PIPELINE

def text_to_speech_kokoro(
    text: str,
    voice: str = "af_bella",
    lang_code: str = "a"
) -> bytes:
    if not text.strip():
        raise ValueError("Empty text provided for TTS")

    pipeline = get_kokoro_pipeline(lang_code)

    try:
        generator = pipeline(text, voice=voice)
        audio_chunks = []
        for _, _, audio in generator:
            audio_chunks.append(audio)

        if not audio_chunks:
            raise RuntimeError("No audio generated by Kokoro")

        audio = np.concatenate(audio_chunks)
        audio_int16 = np.int16(audio * 32767)

        byte_io = io.BytesIO()
        with sf.SoundFile(byte_io, mode='x', samplerate=24000, channels=1, format='WAV') as f:
            f.write(audio_int16)
        
        return byte_io.getvalue()

    except Exception as e:
        logger.error(f"Kokoro TTS failed: {str(e)}")
        raise RuntimeError(f"TTS generation error: {str(e)}")

