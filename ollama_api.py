def extract_patient_facts_only(text: str) -> str:
    """
    Extract ONLY factual patient statements, remove all conversational AI content
    """
    if not text:
        return ""

    lines = text.split("\n")
    patient_facts = []
    
    for line in lines:
        line_lower = line.strip().lower()
        
        # ✅ FIXED: More precise filtering
        # Skip lines that are clearly AI/doctor responses
        skip_patterns = [
            "doctor:",
            "assistant:",
            "ai assistant",
            "typing...",
            # Questions (but NOT statements that happen to have "what")
            line_lower.strip().endswith("?"),
            # AI suggestions/recommendations
            "i recommend",
            "i suggest",
            "you should",
            "it would be best",
            "my recommendation",
        ]
        
        # Check if line should be skipped
        should_skip = False
        for pattern in skip_patterns:
            if isinstance(pattern, bool):
                if pattern:  # If it's True (like the endswith check)
                    should_skip = True
                    break
            elif pattern in line_lower:
                should_skip = True
                break
        
        if should_skip:
            continue
            
        # Skip empty lines
        if not line.strip():
            continue
            
        # Keep patient's factual statements
        if line.strip():
            # Remove "Patient:" prefix if exists, but keep "Image" related content
            cleaned = line.replace("Patient:", "").replace("patient:", "").strip()
            if cleaned and len(cleaned) > 3:  # Avoid single words
                patient_facts.append(cleaned)
    
    return "\n".join(patient_facts)

