def call_ollama(
    prompt: str, 
    system_prompt: Optional[str] = None, 
    model: str = MODEL_NAME,
    temperature: float = 0.3,
    max_tokens: int = 600
) -> str:
    start_time = time.time()
    logger.info("=" * 50)
    logger.info(f"CALLING OLLAMA MODEL: {model}")
    logger.info(f"Prompt length: {len(prompt)} characters")
    
    try:
        full_prompt = f"{system_prompt}\n\n{prompt}" if system_prompt else prompt
        
        payload = {
            "model": model,
            "prompt": full_prompt,
            "stream": False,
            "options": {
                "temperature": temperature,
                "top_p": 0.9,
                "num_predict": max_tokens,
                "repeat_penalty": 1.1,
                "num_thread": 4
            }
        }
        
        logger.info("Sending request to Ollama...")
        response = requests.post(OLLAMA_URL, json=payload, timeout=180)
        response.raise_for_status()
        
        result = response.json()
        generated_text = result.get("response", "").strip()
        
        if not generated_text:
            raise ValueError("Model returned empty response")
        
        elapsed = time.time() - start_time
        logger.info(f"Response received: {len(generated_text)} characters")
        logger.info(f"Processing time: {elapsed:.2f} seconds")
        logger.info("=" * 50)
        
        return generated_text
    
    except requests.exceptions.Timeout:
        logger.error("Request timeout")
        raise HTTPException(
            status_code=status.HTTP_504_GATEWAY_TIMEOUT,
            detail="Request timed out. The model may be loading. Please try again."
        )
    except requests.exceptions.RequestException as e:
        logger.error(f"Ollama request failed: {str(e)}")
        raise HTTPException(
            status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
            detail=f"Ollama service unavailable: {str(e)}"
        )
    except Exception as e:
        logger.error(f"Unexpected error: {str(e)}")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Error calling Ollama: {str(e)}"
        )


def call_ollama_vision(prompt: str, image_base64: str) -> str:
    start_time = time.time()
    logger.info("=" * 50)
    logger.info("CALLING VISION MODEL: qwen2.5vl:7b")
    logger.info("=" * 50)
    
    try:
        chat_payload = {
            "model": VISION_MODEL,
            "messages": [{
                "role": "user",
                "content": prompt,
                "images": [image_base64]
            }],
            "stream": False,
            "options": {
                "temperature": 0.2,
                "num_predict": 500,
                "top_p": 0.9,
                "num_thread": 4
            }
        }
        
        logger.info("Trying chat endpoint...")
        response = requests.post(OLLAMA_CHAT_URL, json=chat_payload, timeout=200)
        
        if response.status_code == 200:
            result = response.json()
            content = result.get("message", {}).get("content", "").strip()
            if content and len(content) > 20:
                elapsed = time.time() - start_time
                logger.info(f"Vision analysis complete: {len(content)} chars in {elapsed:.2f}s")
                logger.info("=" * 50)
                return content
        
        logger.info("Chat endpoint unavailable, trying generate endpoint...")
        gen_payload = {
            "model": VISION_MODEL,
            "prompt": prompt,
            "images": [image_base64],
            "stream": False,
            "options": {
                "temperature": 0.2,
                "num_predict": 500,
                "num_thread": 4
            }
        }
        
        response = requests.post(OLLAMA_URL, json=gen_payload, timeout=200)
        response.raise_for_status()
        
        result = response.json()
        content = result.get("response", "").strip()
        
        if not content or len(content) < 20:
            raise ValueError("Vision model returned insufficient response")
        
        elapsed = time.time() - start_time
        logger.info(f"Vision analysis complete: {len(content)} chars in {elapsed:.2f}s")
        logger.info("=" * 50)
        return content

    except requests.exceptions.Timeout:
        logger.error("Vision model timeout")
        raise HTTPException(
            status_code=status.HTTP_504_GATEWAY_TIMEOUT,
            detail="Image analysis timed out. Please try again."
        )
    except Exception as e:
        logger.error(f"Vision API error: {str(e)}")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"Image analysis failed: {str(e)}"
        )


def validate_image_file(file: UploadFile) -> bytes:
    if file.content_type not in ALLOWED_IMAGE_TYPES:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Invalid image type. Allowed: {', '.join(ALLOWED_IMAGE_TYPES)}"
        )
    
    contents = file.file.read()
    if len(contents) > MAX_IMAGE_SIZE:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Image too large. Max size: 5 MB"
        )
    
    try:
        img = Image.open(BytesIO(contents))
        img.verify()
        
        img = Image.open(BytesIO(contents))
        
        max_dimension = 1536
        if max(img.size) > max_dimension:
            ratio = max_dimension / max(img.size)
            new_size = tuple(int(dim * ratio) for dim in img.size)
            img = img.resize(new_size, Image.Resampling.LANCZOS)
            
            output = BytesIO()
            img.save(output, format=img.format or 'JPEG', quality=90)
            contents = output.getvalue()
            logger.info(f"Image resized to {new_size} for faster processing")
            
    except Exception as e:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail=f"Uploaded file is not a valid image: {str(e)}"
        )
    
    return contents


